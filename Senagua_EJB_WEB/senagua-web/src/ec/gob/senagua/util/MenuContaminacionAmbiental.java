package ec.gob.senagua.util;

public class MenuContaminacionAmbiental {

	private String demarcacionHidrografica;
	private String evento;
	private String fechadeInspeccion;
	private String codigodeFicha;
	private String codigodeInforme;
	private String fechadeInforme;
	private String edicion;

	public String getDemarcacionHidrografica() {
		return demarcacionHidrografica;
	}

	public void setDemarcacionHidrografica(String demarcacionHidrografica) {
		this.demarcacionHidrografica = demarcacionHidrografica;
	}

	public String getEvento() {
		return evento;
	}

	public void setEvento(String evento) {
		this.evento = evento;
	}

	public String getFechadeInspeccion() {
		return fechadeInspeccion;
	}

	public void setFechadeInspeccion(String fechadeInspeccion) {
		this.fechadeInspeccion = fechadeInspeccion;
	}

	public String getCodigodeFicha() {
		return codigodeFicha;
	}

	public void setCodigodeFicha(String codigodeFicha) {
		this.codigodeFicha = codigodeFicha;
	}

	public String getCodigodeInforme() {
		return codigodeInforme;
	}

	public void setCodigodeInforme(String codigodeInforme) {
		this.codigodeInforme = codigodeInforme;
	}

	public String getFechadeInforme() {
		return fechadeInforme;
	}

	public void setFechadeInforme(String fechadeInforme) {
		this.fechadeInforme = fechadeInforme;
	}

	public String getEdicion() {
		return edicion;
	}

	public void setEdicion(String edicion) {
		this.edicion = edicion;
	}

}
